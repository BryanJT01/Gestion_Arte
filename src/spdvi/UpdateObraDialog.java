    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        String codigo = this.getCodigo();
        String IB = codigo.substring(0,2);
        if(IB.equals("IB"))
        {
                codigo = codigo.substring(2, 10);
        }
        lblCodigo.setText(codigo);
        for (Obra o: mainForm.obras) {
            if (o.getRegistre().equals("IB" + codigo)){
                lblCodigo.setText(o.getRegistre());
                txtTitulo.setText(o.getTitol());
                txtAny.setText(o.getAny());
                txtFormato.setText(o.getFormat());
                txtAutor.setText(o.getAutor());
                try {
                    BufferedImage selectedImage;
                    if (o.getImagen() != null) {
                        lblImagenName.setText(o.getImagen());
                        selectedImage = ImageIO.read(new File(System.getProperty("user.home") + "\\AppData\\Local\\OpusList\\images\\" + o.getImagen()));
                        ImageIcon icon = mainForm.resizeImageIcon(selectedImage, lblObraImage.getWidth(), lblObraImage.getHeight());
                        lblObraImage.setIcon(icon);
                    }
                } catch (IOException ex) {
                    ex.getStackTrace();
                }

            }
        }
    }//GEN-LAST:event_formWindowOpened

